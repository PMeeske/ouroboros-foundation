namespace Ouroboros.Domain.Reflection;

/// <summary>
/// Cognitive dimensions for capability assessment.
/// Represents different aspects of cognitive ability that can be measured.
/// </summary>
public enum CognitiveDimension
{
    /// <summary>
    /// Logical and analytical reasoning capabilities.
    /// </summary>
    Reasoning,

    /// <summary>
    /// Planning and goal-oriented behavior.
    /// </summary>
    Planning,

    /// <summary>
    /// Learning and knowledge acquisition.
    /// </summary>
    Learning,

    /// <summary>
    /// Memory retention and recall.
    /// </summary>
    Memory,

    /// <summary>
    /// Ability to generalize from specific instances.
    /// </summary>
    Generalization,

    /// <summary>
    /// Creative problem solving and novel solution generation.
    /// </summary>
    Creativity,

    /// <summary>
    /// Understanding and modeling of social interactions.
    /// </summary>
    SocialIntelligence
}
