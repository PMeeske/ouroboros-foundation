namespace Ouroboros.Domain.Benchmarks;

/// <summary>
/// Represents different dimensions of cognitive capabilities that can be benchmarked.
/// </summary>
public enum CognitiveDimension
{
    /// <summary>
    /// Logical reasoning and inference capabilities.
    /// </summary>
    Reasoning,

    /// <summary>
    /// Planning and goal-oriented behavior.
    /// </summary>
    Planning,

    /// <summary>
    /// Learning from examples and adaptation.
    /// </summary>
    Learning,

    /// <summary>
    /// Memory retention and retrieval.
    /// </summary>
    Memory,

    /// <summary>
    /// Generalization to novel situations.
    /// </summary>
    Generalization,

    /// <summary>
    /// Creative problem solving and novel idea generation.
    /// </summary>
    Creativity,

    /// <summary>
    /// Social intelligence and understanding.
    /// </summary>
    SocialIntelligence,
}
